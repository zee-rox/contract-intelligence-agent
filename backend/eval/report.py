from pathlib import Path
from typing import Any


def render_eval_markdown(results: dict[str, Any]) -> str:
    metrics = results["metrics"]
    provider_rows = "\n".join(
        f"| {item['provider']} | {item['status']} | {item['notes']} |" for item in results["provider_comparison"]
    )
    error_analysis = results["error_analysis"]
    return f"""# Evaluation Results

Generated by the executable evaluation harness. Metrics below are actual results from dataset `{results['dataset_version']}`.

## Configuration

- Dataset version: `{results['dataset_version']}`
- Evaluation run ID: `{results['run_id']}`
- Primary provider: `{results['configuration']['llm_provider']}`
- Embedding model: `{results['configuration']['embedding_model']}`
- Retrieval top K: `{results['configuration']['retrieval_top_k']}`
- Retrieval threshold: `{results['configuration']['retrieval_score_threshold']}`

## Metrics

| Metric | Value |
| --- | ---: |
| Clause precision | {metrics['clause']['precision']} |
| Clause recall | {metrics['clause']['recall']} |
| Clause F1 | {metrics['clause']['f1']} |
| Clause boundary support rate | {metrics['clause']['boundary_support_rate']} |
| Risk accuracy | {metrics['risk']['accuracy']} |
| Citation validity | {metrics['citation']['validity_rate']} |
| Answer term recall | {metrics['qa_grounding']['answer_term_recall']} |
| Citation term recall | {metrics['qa_grounding']['citation_term_recall']} |
| Refusal accuracy | {metrics['refusal']['accuracy']} |
| OCR evaluated pages | {metrics['ocr']['evaluated_pages']} |
| Total runtime seconds | {metrics['performance']['total_seconds']} |

## Provider Comparison

| Provider | Status | Notes |
| --- | --- | --- |
{provider_rows}

## Error Analysis

- Weak clause categories: {', '.join(error_analysis['weak_clause_categories']) or 'none observed in this dataset'}
- Boundary failures: {len(error_analysis['boundary_failures'])}
- Risk disagreements: {len(error_analysis['risk_disagreements'])}
- Unsupported answer issues: {len(error_analysis['unsupported_answer_issues'])}
- Citation failures: {len(error_analysis['citation_failures'])}
- Incorrect refusals: {len(error_analysis['incorrect_refusals'])}
- Answer term failures: {len(error_analysis['answer_term_failures'])}
- Citation term failures: {len(error_analysis['citation_term_failures'])}
- OCR degradation: {error_analysis['ocr_degradation']}

### Boundary Failures

{_format_issues(error_analysis['boundary_failures'])}

### Risk Disagreements

{_format_issues(error_analysis['risk_disagreements'])}

### Unsupported Answer Issues

{_format_issues(error_analysis['unsupported_answer_issues'])}

### Citation Failures

{_format_issues(error_analysis['citation_failures'])}

### Incorrect Refusals

{_format_issues(error_analysis['incorrect_refusals'])}

## Machine-Readable Output

The full result JSON is written to `{results['result_path']}`.
"""


def write_eval_markdown(results: dict[str, Any], path: Path) -> None:
    path.write_text(render_eval_markdown(results), encoding="utf-8")


def _format_issues(issues: list[dict[str, Any]]) -> str:
    if not issues:
        return "None observed."
    return "\n".join(f"- `{issue}`" for issue in issues)
